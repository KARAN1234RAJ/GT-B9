// By using charAt() method

const str = "JavaScript is a text-based programming language";

console.log(str.charAt(0));
//output: J

console.log(str.charAt(5));
//output: c

//By using Bracket Notation
const str1 = "JavaScript is a text-based programming language";

console.log(str1[0]);
//output: J

console.log(str1[5]);
//output: c
